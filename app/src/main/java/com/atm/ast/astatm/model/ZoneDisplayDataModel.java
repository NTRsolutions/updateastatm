package com.atm.ast.astatm.model;

/**
 * @author AST Inc.
 */
public class ZoneDisplayDataModel {

    String zn; // Zone Name
    String zd; // Total Sites
    String zv; // Alarm Sites
    String co; // Color Code
    String zid; // Zone ID
    String zmp; // Zone Contact Number


    String lastUpdatedDate;

    public String getZn() {
        return zn;
    }

    public void setZn(String zn) {
        this.zn = zn;
    }

    public String getZd() {
        return zd;
    }

    public void setZd(String zd) {
        this.zd = zd;
    }

    public String getZv() {
        return zv;
    }

    public void setZv(String zv) {
        this.zv = zv;
    }

    public String getCo() {
        return co;
    }

    public void setCo(String co) {
        this.co = co;
    }

    public String getZid() {
        return zid;
    }

    public void setZid(String zid) {
        this.zid = zid;
    }

    public String getZmp() {
        return zmp;
    }

    public void setZmp(String zmp) {
        this.zmp = zmp;
    }

    public String getLastUpdatedDate() {
        return lastUpdatedDate;
    }

    public void setLastUpdatedDate(String lastUpdatedDate) {
        lastUpdatedDate = lastUpdatedDate;
    }


}
