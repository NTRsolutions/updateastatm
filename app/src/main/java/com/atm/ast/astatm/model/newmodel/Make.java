package com.atm.ast.astatm.model.newmodel;

public class Make {
    private int Id;
    public String Name;
    public long EqId;

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public long getEqId() {
        return EqId;
    }

    public void setEqId(long eqId) {
        EqId = eqId;
    }
}
